    <h1>Apa Itu Laravel?</h1>
    <p>Laravel adalah <b>Framework aplikasi web berbasis PHP</b> yang dirancang untuk mempermudah developer
    membangun aplikasi modern.</p>

    <h2>Fitur Utama Laravel</h2>
    <ul>
        <li><b>MVC (Model-View-Contoller)</b> — membuat kode lebih rapih dan terstruktur</li>
        <li><b>Sinaks elegan dan ekspresif</b> — mudah dibaca dan dipelihara.</li>
        <li><b>Routing sederhana</b> — memetakan URL ke fungsi/aksi dengan mudah.</li>
        <li><b>ORM Eloquent</b> — interaksi dengan database tanpa query SQL mentah.</li>
        <li><b>Fitur bawaan lengkap</b> — administrasi, session, middleware, Blade template, API, queue, dan tesing.</li>
        <li><b>Ekosistem luas</b> — banyak package tersedia untuk berbagai kebutuhan.</li>
        <li><b>Komunias besar</b> — dokumentasi dan dukungan komunitas melimpah</li>
    </ul>

    <p>Singkatnya, <i>Laravel</i> adalah alat bantu yang mempercepat dan mempermudah pembuatan aplikasi web dengan PHP, 
        sehingga hasilnya lebih rapi, aman, dan modern</p>
