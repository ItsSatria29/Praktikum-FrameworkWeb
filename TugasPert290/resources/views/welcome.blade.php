    <h1>Framework Web</h1>
    <p>Framework web adalah kerangka kerja yang membantu developer membangun website atau aplikasi web dengan lebih cepat, terstruktur, dan mudah dipelihara. Ibaratnya seperti pondasi dan peralatan siap pakai, sehingga tidak perlu membuat semuanya dari nol.</p>
    
    <h2>Manfaat Framework Web</h2>
    <ul>
        <li>Mempercepat pengembangan — sudah ada fitur dasar seperti routing, autentikasi, dan koneksi database.</li>
        <li>Kode lebih terstruktur — mudah dibaca, dipahami, dan dipelihara.</li>
        <li>Keamanan lebih baik — banyak framework melindungi dari SQL Injection, XSS, CSRF, dll.</li>
        <li>Mendukung kerja tim — standar penulisan kode jelas sehingga memudahkan kolaborasi.</li>
    </ul>

    <h2>Contoh Framework Web Populer</h2>
    <ul>
        <li>Laravel (PHP)</li>
        <li>Django (Python)</li>
        <li>Express.js (Node.js)</li>
        <li>Spring Boot (Java)</li>
        <li>React.js (JavaScript - Frontend)</li>
    </ul>
